package day03_operator;

public class Ex04_Op {

	public static void main(String[] args) {
		/*
		 * 관계연산자
		 * 참과 거짓을 알려준다
		 * true(참), false(거짓)
		 * a = 10, b = 20;
		 * a > b : false
		 * a <= b :true //=을 사용할때는 반드시 오른쪽으로같다
		 * a == b : false //==는 비교해을때 같다라는 의미이다
		 * a != b : ture // !는 같지 않다
		 */
		
		double do1 = 3.0, do2 = 3.1;
		System.out.println(do1 > do2 );
		System.out.println(do1 <= do2 );
		System.out.println(do1 == do2 );
		System.out.println(do1 != do2 );
		
	}
}
